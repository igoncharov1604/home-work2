namespace ConsoleApp2
{
    using System;

    // Абстрактний клас Рослина
    abstract class Рослина
    {
        // Поля базового класу
        public string Назва { get; set; }

        // Конструктор базового класу з параметрами
        public Рослина(string назва)
        {
            Назва = назва;
        }

        // Абстрактні методи
        public abstract void Опис();
        public abstract void Поливати();
    }

    // Похідний клас Дерево
    class Дерево : Рослина
    {
        // Властивості похідного класу Дерево
        public string Порода { get; set; }
        public int Вік { get; set; }

        // Конструктор з параметрами
        public Дерево(string назва, string порода, int вік) : base(назва)
        {
            Порода = порода;
            Вік = вік;
        }

        // Реалізація абстрактних методів
        public override void Опис()
        {
            Console.WriteLine($"Це дерево: {Назва}, порода: {Порода}, вік: {Вік} років.");
        }

        public override void Поливати()
        {
            Console.WriteLine("Поливаємо дерево.");
        }

        // Метод з параметрами для перевірки віку
        public void ПеревіритиДерево(string порода, int вік)
        {
            if (порода == Порода && вік == Вік)
            {
                Console.WriteLine($"Дерево знайдено: Назва: {Назва}, Порода: {Порода}, Вік: {Вік}.");
            }
            else
            {
                Console.WriteLine($"Такого дерева у нас немає: {порода} {вік} років.");
            }
        }

        // Метод для виведення інформації про дерево
        public void ВивестиІнформацію()
        {
            Console.WriteLine($"Назва: {Назва}, Порода: {Порода}, Вік: {Вік} років.");
        }
    }

    // Похідний клас Квіти
    class Квіти : Рослина
    {
        // Властивість похідного класу Квіти
        public string Колір { get; set; }

        // Конструктор з параметрами
        public Квіти(string назва, string колір) : base(назва)
        {
            Колір = колір;
        }

        // Реалізація абстрактних методів
        public override void Опис()
        {
            Console.WriteLine($"Це квітка: {Назва}, колір: {Колір}.");
        }

        public override void Поливати()
        {
            Console.WriteLine("Поливаємо квіти.");
        }

        // Метод для виведення інформації про троянду
        public void ОписТроянди()
        {
            Console.WriteLine("Яка це троянда?");
            Console.WriteLine("Це троянда Блек Меджік. Вона майже чорного кольору.");
        }
    }

    class Program
    {
        static void Main(string[] args)
        {
            // Створення об'єкта класу Дерево
            Дерево яблуня = new Дерево("Яблуня", "Яблуня", 5);
            яблуня.Опис();
            яблуня.Поливати();
            яблуня.ВивестиІнформацію();
            яблуня.ПеревіритиДерево("Яблуня", 5);
            яблуня.ПеревіритиДерево("Груша", 7);

            // Створення об'єкта класу Квіти
            Квіти троянда = new Квіти("Троянда", "Червоний");
            троянда.Опис();
            троянда.Поливати();
            троянда.ОписТроянди();
        }
    }

}
